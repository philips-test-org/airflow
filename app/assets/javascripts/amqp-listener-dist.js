// Connect to a channel over the socket.
var connectToChannel = function connectToChannel(socket, topic, callbacks) {
  var channel = socket.channel(topic, {});
  var presences = {};

  channel.on("new_msg", callbacks.newMsg);

  channel.on("presence_state", function (presenceState) {
    presences = phoenix_2.syncState(presences, presenceState);
    callbacks.updatePresence(presences);
  });

  channel.on("presence_diff", function (diff) {
    presences = phoenix_2.syncDiff(presences, diff);
    callbacks.updatePresence(presences);
  });

  channel.onClose(callbacks.onClose);

  channel.join().receive("ok", function () {
    return createQueue(channel, callbacks);
  }).receive("error", callbacks.joinError);

  return channel;
};

